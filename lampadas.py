# 5) Você está em uma sala com três interruptores, cada um conectado a uma lâmpada em salas diferentes. 
# Você não pode ver as lâmpadas da sala em que está, mas pode ligar e desligar os interruptores quantas vezes quiser. 
# Seu objetivo é descobrir qual interruptor controla qual lâmpada. Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas, 
# qual interruptor controla cada lâmpada?  

# 1 PASSO - identificar os interruptores como A, B e C
# Primeira etapa (na sala dos interruptores) - ligar o A e o B, e manter o C desligado.
# primeira ida à sala das lâmpadas, observar quais estão acesas, duas estarão, são as conectadas pelo interruptor A e B, e uma estará desligada (interruptor C)
# Anotar quais lâmpadas estão acesas e voltar para a sala principal.
# Segunda estapa - Desligar o interruptor B e ligar o C (mantendo o A ligado)
# Segunda ida à sala das lâmpadas. (a lampada que estava acesa e agora está apagada é controlado pelo interruptor B)
# a lampada que permanece acesa nas duas visita é controlada pelo A.
# a lampada que estava apagada e agora está acesa é controlada pelo C.
